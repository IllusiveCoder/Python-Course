# Arithmetic operators
num1 = 10
num2 = 3

addition = num1 + num2
subtraction = num1 - num2
multiplication = num1 * num2
division = num1 / num2
modulus = num1 % num2
exponentiation = num1 ** num2

# Printing the results
print("Addition:", addition)
print("Subtraction:", subtraction)
print("Multiplication:", multiplication)
print("Division:", division)
print("Modulus:", modulus)
print("Exponentiation:", exponentiation)

# Comparison operators
age = 25
is_adult = age >= 18
is_teenager = age >= 13 and age <= 19

# Printing the results
print("Is Adult:", is_adult)
print("Is Teenager:", is_teenager)

# Logical operators
is_sunny = True
is_warm = False

go_outside = is_sunny and is_warm
stay_inside = not go_outside

# Printing the results
print("Go Outside:", go_outside)
print("Stay Inside:", stay_inside)

# String expressions
greeting = "Hello"
name = "John"

message = greeting + " " + name + "!"
repeated_message = message * 3

# Printing the results
print("Message:", message)
print("Repeated Message:", repeated_message)
