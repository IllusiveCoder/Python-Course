# This program calculates the area of a rectangle
# based on user-provided dimensions.

# User input for length and width
length = float(input("Enter the length of the rectangle: "))  # Get the length from the user
width = float(input("Enter the width of the rectangle: "))    # Get the width from the user

# Calculate the area of the rectangle
area = length * width  # Formula: area = length * width

# Display the result
print("The area of the rectangle is:", area)  # Print the calculated area

# This program follows the standard formula for calculating
# the area of a rectangle, which is length multiplied by width.
# It prompts the user to enter the dimensions and then performs
# the necessary calculations. Finally, it displays the result.

# Note: The dimensions entered by the user are assumed to be
# valid and the program does not include error handling.
