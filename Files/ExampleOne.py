# Variable assignment
name = "John"
age = 25
height = 1.85
is_student = True

# Print the values of the variables
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)

# Performing operations with variables
birth_year = 2021 - age
future_age = age + 10
bmi = 75 / (height ** 2)

# Print the results of the operations
print("Birth Year:", birth_year)
print("Age in 10 Years:", future_age)
print("BMI:", bmi)

# Working with different data types
quote = "Python is a powerful programming language."
programming_languages = ["Python", "Java", "C++", "JavaScript"]
person = {"name": "John", "age": 25, "country": "USA"}

# Accessing elements in lists and dictionaries
first_language = programming_languages[0]
person_name = person["name"]

# Printing the values
print("Quote:", quote)
print("Programming Languages:", programming_languages)
print("First Language:", first_language)
print("Person:", person)
print("Person's Name:", person_name)
